package com.basak.app.model;

public class SnackVO {

	private int snackId, snackCompany, snackCategory, snackCal;
	private String snackNAME, snackIMG, snackCategoryS, snackCompanyS;
	
	public int getSnackId() {
		return snackId;
	}
	public void setSnackId(int snackId) {
		this.snackId = snackId;
	}
	public String getSnackNAME() {
		return snackNAME;
	}
	public void setSnackNAME(String snackNAME) {
		this.snackNAME = snackNAME;
	}
	public String getSnackIMG() {
		return snackIMG;
	}
	public void setSnackIMG(String snackIMG) {
		this.snackIMG = snackIMG;
	}
	public int getSnackCompany() {
		return snackCompany;
	}
	public void setSnackCompany(int snackCompany) {
		this.snackCompany = snackCompany;
	}
	public int getSnackCategory() {
		return snackCategory;
	}
	public void setSnackCategory(int snackCategory) {
		this.snackCategory = snackCategory;
	}
	public int getSnackCal() {
		return snackCal;
	}
	public void setSnackCal(int snackCal) {
		this.snackCal = snackCal;
	}
	public String getSnackCategoryS() {
		return snackCategoryS;
	}
	public void setSnackCategoryS(String snackCategoryS) {
		this.snackCategoryS = snackCategoryS;
	}
	public String getSnackCompanyS() {
		return snackCompanyS;
	}
	public void setSnackCompanyS(String snackCompanyS) {
		this.snackCompanyS = snackCompanyS;
	}
}